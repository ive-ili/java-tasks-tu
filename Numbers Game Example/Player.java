package numbers;

class Player {
    private String name;
    private int score;

    public Player(String name) {
        this.name = name;
        this.score = 0;
    }

    public String getName() {
        return name;
    }

    public int getScore() {
        return score;
    }

    public void updateScore(int attempts) {
        this.score = Math.max(0, NumberGame.MAX_ATTEMPTS - attempts);
    }
}